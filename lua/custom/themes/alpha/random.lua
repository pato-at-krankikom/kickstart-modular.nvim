-- Function to get a banner by name or random if not found
local function get_banner(name)
  if name and name ~= 'random' and banners[name] then
    return banners[name]
  else
    local keys = vim.tbl_keys(banners)
    math.randomseed(os.time())
    return banners[keys[math.random(#keys)]]
  end
end

local function create_header(name)
  return {
    type = 'text',
    val = get_banner(name),
    opts = {
      position = 'center',
      hl = 'Type',
    },
  }
end

local footer = {
  type = 'text',
  val = '',
  opts = {
    position = 'center',
    hl = 'Number',
  },
}

local function create_config(name)
  local section = {
    header = create_header(name),
    footer = footer,
  }

  return {
    layout = {
      {
        type = 'padding',
        val = function()
          return math.floor(vim.fn.winheight(0) * 0.3)
        end,
      },
      section.header,
      { type = 'padding', val = 1 },
      section.footer,
      {
        type = 'padding',
        val = function()
          return math.floor(vim.fn.winheight(0) * 0.3)
        end,
      },
    },
    opts = {
      margin = function()
        local win_width = vim.fn.winwidth(0)
        local total_width = 69 -- Adjust this to match the width of your widest banner
        return math.floor((win_width - total_width) / 2)
      end,
      position = 'center',
      redraw_on_resize = true,
    },
  }
end

return {
  config = create_config,
}
